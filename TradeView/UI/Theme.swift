import SwiftUI

enum Theme {
    enum Colors {
        static let green = Color.green
        static let gray = Color.gray
        static let red = Color.red
        
        static let backgroundGreen = Color.green.opacity(0.1)
        static let backgroundRed = Color.red.opacity(0.1)
        
        static let textPrimary = Color.black
        static let textSecondary = Color.black.opacity(0.6)
        static let textTertiary = Color.gray
    }
}

